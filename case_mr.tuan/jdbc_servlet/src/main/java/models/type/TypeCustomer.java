package models.type;

import lombok.*;



public class TypeCustomer {
    int id;
    String name;

    public TypeCustomer() {
    }

    public TypeCustomer(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
